				<h2>Problems</h2>
				<p>I was pretty proefficient in using Photoshop prior to taking on this project, however I did encounter a few design problems. This was as much an exercise in keeping to a fixed aesthetic as it was fitting that aesthetic to different identities. The bulk of my time was spent researching about the different locations more than I had done before, however it was fun learning about the different locales.</p>

				<p>In addition to research, I had a little problem in getting the correct font to display perfectly in the crest's banner. Using Photoshop's warp tools to bend the text created some streching and kerning problems. In the future, I plan on spending more time researching ways to optimize displaying of curved text in a way that is aesthetically pleasing and time-effective.</p>
