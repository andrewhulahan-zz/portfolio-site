				<h2>Process</h2>
				<p>I was browsing the web when I saw the opportunity to create a crest for a soccer team supporter's group. I first sought to create a crest based on the existing Everton crest for a Chicago supporter's group. My initial thought process was in finding out how to best tie a sense of Chicago identity into the crest while keeping the overall Everton feel. After some research, I decided on replacing the Everton Lock-Up with the Sears Tower while incorporating elements of the city flag of Chicago on to the crest. Keeping with the classic style of the Everton crest, I was able to create an outline silhouette of the Sears Tower that looks both modern and classic. Two white bars and four red 6-pointed stars incorporated the flag to further diversify this crest from the original.</p>

				<p>The second crest was requested by a Hong Kong based supporter's group. In keeping with the first crest, I looked for an appropriate building as a focal point and settled on the Bank of China Tower. I then looked to try incorporating the flag of Hong Kong in the design but had no luck fitting the design in a way that was aesthetically pleasing. Looking elsewhere, I settled on using a Chinese Junk, a common sight in the port city.</p>

